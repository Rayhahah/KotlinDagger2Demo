@Module
class SecondModule {
    @Provides
    fun providerSecondService(secondServiceImpl: SecondServiceImpl): SecondService {
        return secondServiceImpl
    }

    @NameQualifier
    @Provides
    fun providerNameStr(): String {
        return "name"
    }


    @AgeQualifier
    @Provides
    fun providerAgeStr(): String {
        return "age"
    }

    @Provides
    fun providerInt(): Int {
        return 10
    }

    @Named("id")
    @Provides
    fun providerId(): Int {
        return 1
    }


}